      "\n",
      "    def sample(self, batch_size: int):\n",
      "        batch = random.sample(self.buf, batch_size)\n",
      "        s, a, r, s2, d = map(np.array, zip(*batch))\n",
      "        return (\n",
      "            torch.tensor(s,  dtype=torch.float32, device=DEVICE),\n",
      "            torch.tensor(a,  dtype=torch.float32, device=DEVICE),\n",
      "            torch.tensor(r,  dtype=torch.float32, device=DEVICE).unsqueeze(1),\n",
      "            torch.tensor(s2, dtype=torch.float32, device=DEVICE),\n",
      "            torch.tensor(d,  dtype=torch.float32, device=DEVICE).unsqueeze(1),\n",
      "        )\n",
      "\n",
      "    def __len__(self):\n",
      "        return len(self.buf)\n",
      "\n",
      "\n",
      "class OUNoise:\n",
      "    \"\"\"Ornstein-Uhlenbeck noise for DDPG exploration.\"\"\"\n",
      "    def __init__(self, dim: int, mu: float = 0.0, theta: float = 0.15,\n",
