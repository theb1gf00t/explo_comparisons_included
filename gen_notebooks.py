      "        self.dim   = dim\n",
      "        self.mu    = mu\n",
      "        self.theta = theta\n",
      "        self.sigma = sigma\n",
      "        self.state = np.zeros(dim)\n",
      "\n",
      "    def reset(self):\n",
      "        self.state = np.zeros(self.dim)\n",
      "\n",
      "    def sample(self):\n",
